    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        boolean encontrado = false;
        if(!txtbuscarmedico.getText().isEmpty()){
            String criterio = txtbuscarmedico.getText();
            for (Medico medico : listaMedicos) {
                if(medico.getNombre().equals(criterio) || 
                        medico.getCMP().equals(criterio)){
                    modeloListPacientes.removeAllElements();
                    modeloListPacientes.addElement(medico.toString());
                    encontrado = true;
                    JOptionPane.showMessageDialog(null,"Medico "+
                    criterio+" econtrado","Buscar Medico"
                    , JOptionPane.INFORMATION_MESSAGE);
                }
            }
            if(encontrado==false){
            JOptionPane.showMessageDialog(null, "Medico "+criterio
                    +" no econtrado","Buscar Medico"
                    , JOptionPane.INFORMATION_MESSAGE);
            
        }
        }else {
            JOptionPane.showMessageDialog(null, "Ingrese el nombre o CMP del Medico"
                    ,"Buscar Medico", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

