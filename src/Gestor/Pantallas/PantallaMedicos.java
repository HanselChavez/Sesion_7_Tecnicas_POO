    private void txtbuscarmedicoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtbuscarmedicoKeyTyped
         if(!txtbuscarmedico.getText().isEmpty()){
            modeloListPacientes.removeAllElements();
            String criterio = txtbuscarmedico.getText();
            for (Medico medico : listaMedicos) {
                if(medico.getNombre().contains(criterio)||
                        medico.getCMP().contains(criterio)){                      
                    modeloListPacientes.addElement(medico.toString());
                }
            }
        }else if(txtbuscarmedico.getText().isEmpty()){           
            llenarLista();
        }
    }//GEN-LAST:event_txtbuscarmedicoKeyTyped

    private void lstMedicosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstMedicosMouseClicked
        int k = lstMedicos.getSelectedIndex();
        Medico seleMedico;
        if(k!=-1){           
            String j = lstMedicos.getSelectedValue().trim();
            for (int i = 0; i < listaMedicos.size(); i++) {
                if(listaMedicos.get(i).toString().equals(j)){      
                    seleMedico = listaMedicos.get(i);
                    padre.cargarMedico(seleMedico);
                    padre.cargarFechasYHoras();
                    padre.setVisible(true);
                    this.dispose();
                }
            }
        }
    }//GEN-LAST:event_lstMedicosMouseClicked

