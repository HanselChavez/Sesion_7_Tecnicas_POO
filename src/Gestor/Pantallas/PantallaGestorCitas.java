    private void verificarSeleccion(int i, String j) {
        Cita seleCita;
        if(listadeCitas.get(i).toString().equals(j)){
            seleCita = listadeCitas.get(i);
            PantallaEditarCita visualizarCita
                    = new PantallaEditarCita(this,seleCita);
            visualizarCita.setSize(270,400);
            visualizarCita.setLocationRelativeTo(null);
            visualizarCita.setVisible(true);
            this.setVisible(false);
        }
    }
    public void llenarLista(){
        modeloListCitas = new DefaultListModel();
        lstCitas.setModel(modeloListCitas);
        modeloListCitas.removeAllElements();
        
        for (Cita cita : listadeCitas) {
            modeloListCitas.addElement(cita.toString());
        }
    }
    public void agregarCita(Cita cita){
        listadeCitas.add(cita);
        llenarLista();
    }   
