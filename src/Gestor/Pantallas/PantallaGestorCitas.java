    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
       
        boolean encontrado = false;
        if(!txtBuscar.getText().isEmpty()){
            String campoingresado = txtBuscar.getText();
            for (Cita cita : listadeCitas) {
                if(cita.getPaciente().getDni().equals(campoingresado) || 
                    cita.getPaciente().getNombre().equals(campoingresado)){
                    modeloListCitas.removeAllElements();
                    modeloListCitas.addElement(cita.toString());
                    encontrado = true;
                    JOptionPane.showMessageDialog(null, "Cita del paciente "+
                    campoingresado+" econtrado","Buscar Cita"
                    , JOptionPane.INFORMATION_MESSAGE);
                }
            }
            if(encontrado==false){
            JOptionPane.showMessageDialog(null, "Cita del paciente "+
                    campoingresado+" no econtrado","Buscar Cita"
                    , JOptionPane.INFORMATION_MESSAGE);
            
        }
        }else {
            JOptionPane.showMessageDialog(null, "Ingrese el dni o nombre del paciente"
                    ,"Buscar Paciente", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

