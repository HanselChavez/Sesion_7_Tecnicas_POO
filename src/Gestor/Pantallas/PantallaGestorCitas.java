    private void btnSalirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalirActionPerformed
        // TODO add your handling code here:
        int k = lstCitas.getSelectedIndex();       
        if(k!=-1 ){           
            String j = lstCitas.getSelectedValue().trim();
            for (int i = 0; i < listadeCitas.size(); i++) {
                if(listadeCitas.get(i).toString().equals(j)){
                    listadeCitas.remove(i);
                    llenarLista();
                }
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Seleccione una cita de la lista"
                ,"Buscar Cita",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnSalirActionPerformed

    private void lstCitasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstCitasMouseClicked
        // TODO add your handling code here:     
        int k = lstCitas.getSelectedIndex();       
        if(k!=-1 && evt.getClickCount() == 2){
           
            String j = lstCitas.getSelectedValue().trim();
            for (int i = 0; i < listadeCitas.size(); i++) {
                verificarSeleccion(i, j);
            }
        }
    }//GEN-LAST:event_lstCitasMouseClicked
   
    private void txtBuscarKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarKeyTyped
        // TODO add your handling code here:
        if(!txtBuscar.getText().isEmpty()){
            modeloListCitas.removeAllElements();
            String campoingresado = txtBuscar.getText();
            for (Cita cita : listadeCitas) {                
                if(cita.getPaciente().getDni().contains(campoingresado) || 
                    cita.getPaciente().getNombre().contains(campoingresado)){
                    modeloListCitas.removeAllElements();
                    modeloListCitas.addElement(cita.toString());                   
                }
            }
        }else if(txtBuscar.getText().isEmpty()){           
            llenarLista();
        }
    }//GEN-LAST:event_txtBuscarKeyTyped

