    private void lstpacientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstpacientesMouseClicked
        int k = lstpacientes.getSelectedIndex();
        Paciente selecPaciente;
        if(k!=-1){           
            String j = lstpacientes.getSelectedValue().trim();
            for (int i = 0; i < listaPacientes.size(); i++) {
                if(listaPacientes.get(i).toString().equals(j)){      
                   selecPaciente = listaPacientes.get(i);
                   padre.cargarPaciente(selecPaciente);
                   padre.setVisible(true);
                   this.dispose();
                }
            }
        }
    }//GEN-LAST:event_lstpacientesMouseClicked

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        boolean encontrado = false;
        if(!txtbuscarpaciente.getText().isEmpty()){
            String dni = txtbuscarpaciente.getText();
            for (Paciente paciente : listaPacientes) {
                if(paciente.getDni().equals(dni)){
                    modeloListPacientes.removeAllElements();
                    modeloListPacientes.addElement(paciente.toString());
                    encontrado = true;
                    JOptionPane.showMessageDialog(null,"Paciente con NÂ° de dni "+
                    dni+" econtrado","Buscar Paciente"
                    , JOptionPane.INFORMATION_MESSAGE);
                }
            }
            if(encontrado==false){
            JOptionPane.showMessageDialog(null, "Paciente con dni "+dni
                    +" no econtrado","Buscar Paciente"
                    , JOptionPane.INFORMATION_MESSAGE);
            
        }
        }else {
            JOptionPane.showMessageDialog(null, "Ingrese el dni del paciente"
                    ,"Buscar Paciente", JOptionPane.INFORMATION_MESSAGE);
        }
        
        
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void txtbuscarpacienteKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtbuscarpacienteKeyTyped
        
        if(!txtbuscarpaciente.getText().isEmpty()){
            modeloListPacientes.removeAllElements();
            String dni = txtbuscarpaciente.getText();
            for (Paciente paciente : listaPacientes) {
                if(paciente.getDni().contains(dni)){                      
                    modeloListPacientes.addElement(paciente.toString());
                }
            }
        }else if(txtbuscarpaciente.getText().isEmpty()){           
            llenarLista();
        }
    }//GEN-LAST:event_txtbuscarpacienteKeyTyped

