    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        String rpta = "";
         rpta = JOptionPane.showInputDialog(null, "Ingrese especialidad: "
                , "Agregar especialidad",3);
        if(!"".equals(rpta)){
            Especialidad especialidad = new Especialidad();
            especialidad.setNombre(rpta);
            listaEspecialidad.add(especialidad);            
            llenarLista();
            
        }else{
            JOptionPane.showMessageDialog(null, "Debe ingresar una especialidad"
                    ,"Agregar especialidad", JOptionPane.INFORMATION_MESSAGE);
        }
       
    }//GEN-LAST:event_btnaddActionPerformed

    private void btnquitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnquitarActionPerformed
        int k = lstespecialidades.getSelectedIndex();        
        if(k!=-1){           
            String j = lstespecialidades.getSelectedValue().trim();
            for (int i = 0; i < listaEspecialidad.size(); i++) {
                if(listaEspecialidad.get(i).getNombre().equals(j)){
                    listaEspecialidad.remove(i);
                    llenarLista();
                }
            }      
        }
        else{
            JOptionPane.showMessageDialog(null, "Selecciones una especialidad de la lista"
                    ,"Eliminar especialidad", JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_btnquitarActionPerformed

    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
        if( !txtCMPmedico.getText().isEmpty() && 
            !txtNombremedico.getText().isEmpty() && 
            !listaEspecialidad.isEmpty() ){       
            guardarCita();
            this.dispose();
            padre.llenarLista();
            padre.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(null, "Complete todos los campos"
                    ,"Agregar medico", JOptionPane.INFORMATION_MESSAGE);
        
        }
       
    }//GEN-LAST:event_btnguardarActionPerformed

