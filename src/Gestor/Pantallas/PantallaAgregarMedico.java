    private void guardarCita() {
        String nombre = txtNombremedico.getText();
        String cmp = txtCMPmedico.getText();
        medico.setCMP(cmp);
        medico.setNombre(nombre);
        medico.setEspecialidades(listaEspecialidad);
        padre.agregarMedico(medico);
    }
    public void llenarLista(){
        modelolistaEsp = new DefaultListModel();
        lstespecialidades.setModel(modelolistaEsp);
        modelolistaEsp.removeAllElements();
        
        listaEspecialidad.forEach((especialidad) -> {
            modelolistaEsp.addElement(especialidad.getNombre());
        });
    }
